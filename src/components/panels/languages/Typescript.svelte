<Article title="Typescript">
  <p>
    The only thing better for largescale portable web applications than
    Javascript, is Javascript with types.
  </p>
  <p>
    Types are an incredible tool for safety and communication of intent with
    future developers of our software. Typescript does an amazing job of keeping
    the dynamicism of Javascript, but with some added guard rails and hints.
  </p>
  <p><i>I'm a big fan.</i></p>
  <p>
    I have a particular interest in how much can be pushed into the compiler,
    for instance I've seen the power of leveraging tools like graphql-codegen
    for adding typesafety between isolated systems.
  </p>
